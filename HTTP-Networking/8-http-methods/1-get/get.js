/* The GET method
eng

The GET method is used to 'get' a representation of a specified resource. 
You are not taking the data away from the server, but rather getting a representation, 
or copy, of the resource in its current state. 
A get request is considered a safe method to call multiple times 
because it doesn't alter the state of the server.

---------------------------------------------------------
spa

El metodo GET se usa para "obtener" una representacion de un recurso especifico.
No esta sacando los datos del servidor, sino obteniendo una representacion,
o copia, del recurso en su estado actual.
Una solicitud de obtencion se considera un metodo seguro para llamar varias veces 
porque no altera el estado del servidor.
*/