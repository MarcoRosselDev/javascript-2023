// MOD, %     <---------- representacion

// el modulo es lo que queda de la division

let a = 5;
let b = 7;

console.log(a % b); // 5/7 ---> no se puede por lo que es = 5
console.log(b % a); // 7/5 ---> si se puede por lo que es = 2 (lo que queda)
console.log(10 % 4); // 10/4 = 8 y quedan 2 por lo que es = 2
console.log(9 % 10); // = 9
// cuando el numerador es menor que el divisor el modulo es el numerador

