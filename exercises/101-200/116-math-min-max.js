// ejercicio 116: obterner el minimo y el maximo de un conjunto de numeros

let numeros = [7, 11, 3, -4, 6, -22, 5, 514, 1, 5, 0];

console.log(`el valor minimo del arreglo numeros es ${Math.min(...numeros)}`); // -22
console.log(`el valor maximo del arreglo numeros es ${Math.max(...numeros)}`); // 514
