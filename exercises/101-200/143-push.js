// ejercicio 143: agregar elementos a un arreglo con el metodo push.

let lenguajes = ["java", "JavaScript", "c++", "python"];

console.log(lenguajes); // [ 'java', 'JavaScript', 'c++', 'python' ]
console.log(lenguajes.length); // 4

console.log();

lenguajes.push("PHP");
console.log(lenguajes); // [ 'java', 'JavaScript', 'c++', 'python', 'PHP' ]
console.log(lenguajes.length); // 5
